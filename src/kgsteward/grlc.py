SH   = Namespace( "http://www.w3.org/ns/shacl#" )
SCH  = Namespace( "https://schema.org/" )
SPEX = Namespace( "https://purl.expasy.org/sparql-examples/ontology#" )
KG   = Namespace( "https://purl.expasy.org/kgsteward/" )
DCT  = Namespace( "http://purl.org/dc/terms/" )

_FALLBACK_BASE = str( KG ) + "query/"

_FORM_TO_SH = {
    "SELECT"    : ( SH.SPARQLSelectExecutable,    SH.select    ),
    "ASK"       : ( SH.SPARQLAskExecutable,       SH.ask       ),
    "CONSTRUCT" : ( SH.SPARQLConstructExecutable, SH.construct ),
    "DESCRIBE"  : ( SH.SPARQLDescribeExecutable,  SH.describe  ),
    "UPDATE"    : ( SH.SPARQLUpdateExecutable,    SH.update    ),
}

_WIDGET_CLASS = {
    "select"       : KG.SelectParam,
    "checkbox"     : KG.CheckboxParam,
    "autocomplete" : KG.AutocompleteParam,
    "text"         : KG.TextParam,
    "slider"       : KG.SliderParam,
}
_QUERY_BACKED = { "select", "checkbox", "autocomplete" }

def _bind( g ):
    g.bind( "sh", SH ); g.bind( "schema", SCH ); g.bind( "spex", SPEX )
    g.bind( "rdfs", RDFS ); g.bind( "kgsteward", KG ); g.bind( "dct", DCT )

def subject_iri( name, endpoint = None ):
    base = ( endpoint.rstrip( "/" ) + "/.well-known/sparql-examples/"
             if endpoint else _FALLBACK_BASE )
    return URIRef( base + name )

def _add_index( g, subj, idx_pred, var_pred, val ):
    if isinstance( val, bool ):                    # guard: bool is an int subclass
        val = int( val )
    if isinstance( val, int ):
        g.add(( subj, idx_pred, RdfLiteral( val, datatype = XSD.integer )))
    else:
        g.add(( subj, var_pred, RdfLiteral( str( val ))))

def _add_widget( g, w, default_options ):
    subj = KG[ w.id ]
    g.add(( subj, RDF.type, _WIDGET_CLASS[ w.type ] ))
    g.add(( subj, KG.prompt, RdfLiteral( w.prompt )))
    if w.type in _QUERY_BACKED:
        g.add(( subj, KG.selectQuery, KG[ w.options or default_options ] ))
        _add_index( g, subj, KG.valueIndex, KG.valueVar, w.valueIndex )
        _add_index( g, subj, KG.nameIndex,  KG.nameVar,  w.nameIndex )
    elif w.type == "text":
        if w.pattern:
            g.add(( subj, KG.pattern, RdfLiteral( w.pattern )))
    elif w.type == "slider":
        g.add(( subj, KG.min,  RdfLiteral( w.min )))
        g.add(( subj, KG.max,  RdfLiteral( w.max )))
        g.add(( subj, KG.step, RdfLiteral( w.step )))

def _add_form( g, name, fields ):
    form = KG[ "form_" + name ]
    g.add(( form, RDF.type, KG.InputForm ))
    g.add(( form, KG.selectQuery, KG[ name ] ))
    nodes = []
    for f in fields:
        b = BNode()
        g.add(( b, KG.targetStr,  RdfLiteral( f.target )))
        g.add(( b, KG.replaceWith, KG[ f.widget ] ))
        if f.default is not None:
            g.add(( b, KG.default, RdfLiteral( f.default )))
        nodes.append( b )
    lst = BNode()
    Collection( g, lst, nodes )
    g.add(( form, KG.inputField, lst ))

def build_graph( text, name, decorators, endpoint = None, form = None, graph = None ):
    """Add RDF documenting the query (sparql-examples + query-form extension)
    into *graph* (created if None) and return it. Query text is stored as
    authored."""
    g = graph
    if g is None:
        g = Graph(); _bind( g )
    subj = subject_iri( name, endpoint )

    g.add(( subj, RDF.type, SH.SPARQLExecutable ))
    if form in _FORM_TO_SH:
        cls, pred = _FORM_TO_SH[ form ]
        g.add(( subj, RDF.type, cls ))
        # Prepend a '#+ endpoint:' grlc directive so the published query is
        # self-describing, unless it already declares its own endpoint.
        own_endpoint = decorators.endpoint if decorators else None
        query_text = text
        if endpoint and not own_endpoint:
            query_text = "#+ endpoint: " + endpoint + "\n" + text
        g.add(( subj, pred, RdfLiteral( query_text )))

    if decorators:
        if decorators.summary:
            g.add(( subj, RDFS.label, RdfLiteral( decorators.summary )))
        comment = decorators.description or decorators.summary
        if comment:
            g.add(( subj, RDFS.comment, RdfLiteral( comment )))
        target = decorators.endpoint or endpoint
        if target:
            g.add(( subj, SCH.target, URIRef( target )))
        for tag in ( decorators.tags or [] ):
            g.add(( subj, SCH.keywords, RdfLiteral( tag )))
        if decorators.source:
            g.add(( subj, DCT.source, RdfLiteral( decorators.source )))
        if decorators.citation:
            g.add(( subj, SCH.citation, RdfLiteral( decorators.citation )))
        for w in ( decorators.selector or [] ):
            _add_widget( g, w, default_options = name )   # options default to this query
        if decorators.form:
            _add_form( g, name, decorators.form )

    return g

# --------------------------------------------------------------------------- #
# Per-query record & accumulating catalogue
# --------------------------------------------------------------------------- #

class GrlcQuery:
    """One parsed query: executable text + metadata + its IRI in the graph."""
    def __init__( self, name, text, query, decorators, form, subject ):
        self.name       = name
        self.text       = text        # as authored
        self.query      = query       # executable (defaults applied)
        self.decorators = decorators  # GrlcDecorators | None
        self.form       = form        # SELECT/ASK/... | None
        self.subject    = subject     # URIRef

class GrlcCatalog:
    """Accumulates parsed queries and their documentation into one graph.

    The graph is the single backing store consumed both by the sparql-editor
    (:meth:`serialize`) and by an MCP server (:meth:`list` / :meth:`get` /
    :meth:`search`, or SPARQL over ``self.graph``)."""

    def __init__( self ):
        self.graph = Graph(); _bind( self.graph )
        self.queries = []
        self._by_name = {}

    def parse( self, text, name, endpoint = None ):
        """Parse one query file, fold its documentation into the graph, and
        return the :class:`GrlcQuery`."""
        decorators = parse_decorators( text )
        form       = detect_form( text )
        query      = apply_defaults( text, decorators )
        subject    = subject_iri( name, endpoint )
        build_graph( text, name, decorators, endpoint = endpoint, form = form, graph = self.graph )
        q = GrlcQuery( name, text, query, decorators, form, subject )
        self.queries.append( q )
        self._by_name[ name ] = q
        return q

    def get( self, name ):
        return self._by_name.get( name )

    def list( self ):
        """Lightweight catalogue entries (name, form, summary, tags)."""
        out = []
        for q in self.queries:
            d = q.decorators
            out.append({
                "name"    : q.name,
                "form"    : q.form,
                "summary" : d.summary if d else None,
                "tags"    : d.tags if d else None,
            })
        return out

    def search( self, text = None, tags = None ):
        """Return GrlcQuery objects matching a substring (name/summary/
        description) and/or any of *tags*."""
        res = []
        for q in self.queries:
            d = q.decorators
            hay = " ".join( filter( None, [
                q.name,
                d.summary if d else None,
                d.description if d else None,
            ])).lower()
            if text and text.lower() not in hay:
                continue
            if tags:
                qtags = set( d.tags or [] ) if d else set()
                if not ( set( tags ) & qtags ):
                    continue
            res.append( q )
        return res

    def check( self ):
        """Cross-query validation, run after all files are parsed. Returns a
        list of human-readable problems (empty when consistent)."""
        problems  = []
        widget_ct = {}                                   # widget id -> definition count
        names     = { q.name for q in self.queries }
        for q in self.queries:
            d = q.decorators
            if not d:
                continue
            for w in ( d.selector or [] ):
                widget_ct[ w.id ] = widget_ct.get( w.id, 0 ) + 1
                opt = getattr( w, "options", None )
                if opt and opt not in names:
                    problems.append( f"{q.name}: selector '{w.id}' options query '{opt}' not found" )
        for wid, c in widget_ct.items():
            if c > 1:
                problems.append( f"duplicate selector id '{wid}' ({c} definitions)" )
        for q in self.queries:
            d = q.decorators
            if not d:
                continue
            body = strip_comments( q.text )    # ignore the #+ block: target must be in the executable body
            for f in ( d.form or [] ):
                if f.widget not in widget_ct:
                    problems.append( f"{q.name}: form references undefined widget '{f.widget}'" )
                if f.target not in body:
                    problems.append( f"{q.name}: form targetStr {f.target!r} not found in query body" )
        return problems

    def serialize( self, format = "turtle" ):
        return self.graph.serialize( format = format )

# --------------------------------------------------------------------------- #
# Single-query convenience (thin wrapper over GrlcCatalog)
# --------------------------------------------------------------------------- #

def parse_query( text, name, endpoint = None ):
    """Parse one query, returning ``(executable_query, rdflib.Graph)``."""
    cat = GrlcCatalog()
    q = cat.parse( text, name, endpoint )
    return q.query, cat.graph
