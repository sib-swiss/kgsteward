"""grlc decorator support for kgsteward SPARQL query files.

A query file may carry `grlc <https://grlc.io>`_ decorators as a block of
`#+` YAML comment lines. This module

  * parses the `#+` block into a validated :class:`GrlcDecorators` object,
  * substitutes grlc parameter ``defaults`` into the query text,
  * emits RDF documenting the query in the SIB *sparql-examples* vocabulary, and
  * emits, from the query-form extension (``selector`` / ``form`` decorators),
    RDF describing dynamic input forms in the kgsteward vocabulary
    (see ``doc/grlc/queryform-extension.md``).

Queries are accumulated into a :class:`GrlcCatalog`, whose internal graph is a
single backing store for both the sparql-editor (via :meth:`serialize`) and an
MCP server (via :meth:`list`, :meth:`get`, :meth:`search`, or SPARQL over the
graph).

The module is deliberately independent from :mod:`kgsteward.yamlconfig`.  As
with the kgsteward YAML documentation, the decorator reference is autogenerated
from the pydantic models (see ``doc/grlc/make_grlc_README.py``); descriptions
live on the models.
"""

